"""
This module demonstrates basic variable usage and data types in Python.
"""


def main():
    """Main function to display variable values and types."""
    name = "Shweta"
    age = 22
    price = 45.9

    print(name)
    print(price)
    print(age)

    print("My name is:", name)
    print("My age is:", age)

    print(type(name))
    print(type(age))
    print(type(price))


if __name__ == "__main__":
    main()
